function AllProducts() {
    const { getAllProduct } = useContext(MyContext)

    const cartItems = useSelector((state) => state.cart);
    const dispatch = useDispatch();
    const addCart = (item) => {
        dispatch(addToCart(item))
        toast.success("Added to Cart")
    }

    const deleteCart = (item) => {
        dispatch(deleteFromCart(item))
        toast.success("Deleted from Cart")
    }
    useEffect(() => {
        console.log(cartItems);
        localStorage.setItem("cart", JSON.stringify(cartItems))
    }, [cartItems])


    return (
        <Layout>
            <div className=" w-full ">
                <div className=" w-full  md:px-20  py-5">
                    <div className=" w-full">
                        <h1 className=" text-center text-[26px] font-bold mb-5">All Products</h1>

                        <div className=" w-full flex  flex-wrap px-4 md:px-0  flex-col md:flex-row md:justify-between lg:justify-start lg:gap-5 pb-5 ">
                            {getAllProduct.map((product, index) => {
                                return (
                                    <div className=" w-full sm:w-[70%] my-3 mx-auto shadow-lg  rounded-xl overflow-hidden   md:mx-0 md:w-[45%] lg:w-[23%] h-[450px] " key={index}>


                                        <Link to={`/productinfo/${product.id}`}>
                                            <div className=" w-full h-[70%]">
                                                <img className=" w-full h-full" src={product.productImageUrl} alt="" />
                                            </div>
                                        </Link>

                                        <div className=" w-full px-4">
                                            <h4 className=" text-gray-300 font-medium">E-Rahul</h4>

                                            <h2 className=" text-[18px] font-semibold text-ellipsis overflow-hidden whitespace-nowrap w-full">{product.title}</h2>
                                            <h2 className=" font-semibold  my-1">â‚¹ {product.price}</h2>

                                            {cartItems.some((p) => p.id === product.id) ?

                                                <button
                                                    onClick={() => deleteCart(product)}
                                                    className=" bg-pink-800 hover:bg-pink-600 w-full text-white py-[4px] rounded-lg font-bold">
                                                    Delete from Cart
                                                </button> :

                                                <button
                                                    onClick={() => addCart(product)}
                                                    className=" bg-pink-500 hover:bg-pink-600 w-full text-white py-[4px] rounded-lg font-bold">
                                                    Add to Cart
                                                </button>

                                            }

                                        </div>

                                    </div>
                                )
                            })}
                        </div>

                    </div>

                </div>
            </div>


        </Layout>
    )
}

export default AllProducts